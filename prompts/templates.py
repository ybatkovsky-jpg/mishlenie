# --- Thinking types mapping ---

THINKING_TYPES: dict[str, str] = {
    "analytical": "Аналитическое",
    "logical": "Логическое",
    "critical": "Критическое",
    "systemic": "Системное",
    "strategic": "Стратегическое",
    "creative": "Креативное",
    "emotional": "Эмоциональный интеллект",
}

THINKING_TYPE_KEYS = list(THINKING_TYPES.keys())

DIFFICULTY_LEVELS = ["начальный (бытовые сценарии)", "средний (рабочие/социальные)", "продвинутый (абстрактные/многоуровневые)"]


def get_thinking_type_name(key: str) -> str:
    """Get Russian name for thinking type key."""
    return THINKING_TYPES.get(key, key)


def get_difficulty_label(level: int) -> str:
    """Get difficulty label for level 0, 1, or 2."""
    if 0 <= level < len(DIFFICULTY_LEVELS):
        return DIFFICULTY_LEVELS[level]
    return DIFFICULTY_LEVELS[-1]
